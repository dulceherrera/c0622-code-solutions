/* Return the input word to check if it is strictly equal to vowel either uppercase or lowercase. */
function isVowel(char) {
  return ((char === 'a') || (char === 'e') || (char === 'i') || (char === 'o') || (char === 'u') || (char === 'A') || (char === 'E') || (char === 'I') || (char === 'O') || (char === 'U'));
}
